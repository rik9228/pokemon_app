<template>
  <div class="wrapper">
    <form @submit.prevent="setPokemonUrl" action="">
      <input type="text" v-model="searchValue" />
    </form>
    <i class="fas fa-search" @click="setPokemonUrl"></i>
  </div>
</template>

<script>
export default {
  props: ["apiUrl"],
  data() {
    return {
      searchValue: "",
    };
  },
  methods: {
    setPokemonUrl() {
      if (this.searchValue !== "") {
        console.log(this.apiUrl + this.searchValue);
        this.$emit("setPokemonUrl", this.apiUrl + this.searchValue);
      }
    },
  },
};
</script>

